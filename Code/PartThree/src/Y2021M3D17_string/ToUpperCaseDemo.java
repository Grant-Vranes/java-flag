package Y2021M3D17_string;
/**
 * String toUpperCase()
 * String toLowerCase()
 * 将当前字符串中的英文部分转换为全大写或者全小写
 * @author Grant・Vranes
 *
 */
public class ToUpperCaseDemo {
	public static void main(String[] args) {
		String str = "Java";
		System.out.println(str.toUpperCase());
		
		System.out.println(str.toLowerCase());
	}
}
